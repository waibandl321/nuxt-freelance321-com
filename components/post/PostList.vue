<template>
  <div>
    <CommonMessageViewer :message="message" />
    <CommonLoadingPageInner v-if="loading" />
    <v-row v-if="!loading">
      <v-col
        v-for="(item, idx) in posts"
        :key="idx"
        cols="4"
        md="3"
        sm="2"
        xs="1"
      >
        <v-card
          hover
          @click="clickPostCard(item)"
        >
          <v-img
            :src="imagePath(item)"
            aspect-ratio="1.7"
          />
          <v-card-subtitle>
            {{ item.title.rendered }}
          </v-card-subtitle>
        </v-card>
      </v-col>
    </v-row>
    <div class="text-center mt-10 pagination">
      <v-pagination
        v-model="current_page"
        :length="15"
        :total-visible="7"
        @input="$fetch"
      />
    </div>
  </div>
</template>

<script>
// import store from "@/store"
export default {
  name: 'PostList',
  data () {
    return {
      media_base_url: 'https://freelance321.com/wp-content/uploads/',
      posts_base_url: 'https://freelance321.com/wp-json/wp/v2/posts',
      current_page: 1,
      per_page: 8,
      pagination: [],

      posts: [],
      categories: [],

      loading: false,
      message: {
        error: '',
        success: ''
      }
    }
  },
  async fetch () {
    this.loading = true
    try {
      const results = await this.$axios.get(
        this.posts_base_url +
        '?_embed' +
        '&page=' + this.current_page +
        '&per_page=' + this.per_page
      )
      this.posts = results.data
      this.setPaginations(results)
    } catch {
      this.message.error = 'データの読み込みに失敗しました。'
    }
    this.loading = false
  },
  created () {
    this.categories = this.$store.state.category_items
  },
  methods: {
    imagePath (item) {
      return this.media_base_url + item._embedded['wp:featuredmedia'][0].media_details.file
    },
    setPaginations (results) {
      const total_page_num = Math.ceil(results.headers['x-wp-total'] / this.per_page)
      for (let i = 1; i < total_page_num; i++) {
        this.pagination.push(total_page_num[i])
      }
    },
    clickPostCard (post) {
      let parent_category = null
      // 詳細をstoreに保存
      this.$store.dispatch('setPostView', post)
      const category_id = post.categories[0]
      // 現在のカテゴリー
      const current_category = this.categories.find(v => v.id === category_id)
      // 親カテゴリー
      if (current_category.parent !== 0) {
        parent_category = this.categories.find(r => r.id === current_category.parent)
        this.$router.push(parent_category.slug + '/' + current_category.slug + '/' + post.slug)
        return
      }
      this.$router.push(current_category.slug + '/' + post.slug)
    },
    changePage (number) {
      this.posts = []
      this.current_page = number
      this.$fetch()
    }
  }
}
</script>

