<template>
  <div>
    <CommonMessageViewer :message="message" />
    <CommonLoadingPageInner v-if="loading" />
    <v-row v-else>
      <v-col
        v-for="(item, idx) in posts"
        :key="idx"
        cols="4"
      >
        <v-card
          hover
          @click="clickPostCard(item)"
        >
          <v-img
            :src="imagePath(item)"
            aspect-ratio="1.7"
          />
          <v-card-subtitle>
            {{ item.title.rendered }}
          </v-card-subtitle>
        </v-card>
      </v-col>
    </v-row>
    <div
      v-if="!loading"
      class="text-center mt-10 pagination"
    >
      <v-pagination
        v-model="current_page"
        :length="page_max"
        @input="changePage"
      />
    </div>
  </div>
</template>

<script>
export default {
  name: 'CategoryPostList',
  data () {
    return {
      loading: false,
      message: {
        error: '',
        success: ''
      },

      category_id: '',
      posts: [],

      current_page: 1,
      per_page: 8,
      page_max: 1,

      media_base_url: 'https://freelance321.com/wp-content/uploads/'
    }
  },
  async fetch () {
    this.loading = true
    this.category_id = this.$route.query.c
    try {
      this.posts = await this.apiGetCategoryPostList(
        this.$route.query.c,
        this.current_page,
        this.per_page,
        this.apiTypeDefault()
      ).then((response) => {
        this.setPaginations(response)
        return response.data
      })
    } catch {
      this.message.error = 'データの読み込みに失敗しました。'
    }
    this.loading = false
  },
  computed: {
    categories: {
      get () {
        return this.$store.getters.storeGetCategoryItems
      }
    }
  },
  methods: {
    imagePath (item) {
      if (!item._embedded['wp:featuredmedia']) {
        return this.media_base_url + '2021/08/web-productions.jpg'
      }
      return this.media_base_url + item._embedded['wp:featuredmedia'][0].media_details.file
    },
    setPaginations (results) {
      this.page_max = Math.ceil(results.headers['x-wp-total'] / this.per_page)
    },
    clickPostCard (post) {
      // 詳細データ store格納
      this.$store.dispatch('storeSetPostView', this.copyJson(post))
      const current_category = this.categories.find(v => v.id === post.categories[0])
      this.pageMovePost(current_category, post)
    },
    changePage (number) {
      this.posts = []
      this.current_page = number
      this.$fetch()
    }
  }
}
</script>
